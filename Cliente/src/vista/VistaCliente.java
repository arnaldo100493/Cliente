    private void conectar() {
        if (!this.txtIP.getText().isEmpty()) {
            Chat.initClient(this.txtIP.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Debe introducir una direcci√≥n ip", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }

    private void enviar() {
        Chat.cliente.enviarMensaje(this.txtNickname.getText() + " " + "dice: " + "\n" + this.txtMensaje.getText());
        jTextArea1.setText(this.txtNickname.getText() + " " + "dice: " + "\n" + this.txtMensaje.getText());
        this.limpiarComponentes();
    }
    
    private void limpiarComponentes(){
        LimpiarComponentes.limpiarComponentes(this.jPanel3);
    }

